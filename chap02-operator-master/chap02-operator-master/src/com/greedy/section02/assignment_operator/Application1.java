package com.greedy.section02.assignment_operator;

public class Application1 {

	public static void main(String[] args) {

		/* 산술 복합 대입 연산자 */
		int num = 12;

		// 3 증가
		// num = num + 3;
		num += 3;
		System.out.println("num : " + num);

		// 5 감소
		num -= 5;
		System.out.println("num : " + num);

		// 2배 증가
		num *= 2;
		System.out.println("num : " + num);

		// 2배 감소
		num /= 2;
		System.out.println("num : " + num);
		
		
		
		
		
		
		
		
		
		
		
	}

}
