package com.greedy.section01.arithmetic_operator;

public class Application1 {

	public static void main(String[] args) {
		/* 산술 연산자 */
		/* 산술 연산자는 주로 사칙연산과 관련 된 연산자로 가장 기본적이면서도 많이 사용되는 연산자이다.
		 * 연산자의 실행이 가능하기 위해 필요한 값이나 변수가 두 개인 이상 연산자로 분류되며
		 * 피연산자의 연산 방향은 왼쪽에서 오른쪽이다.
		 * */
		
		/* 산술 연산자의 우선 순위
		 * 수학의 개념과 유사하게 곱하기 나누기 연산이 더하기 빼기 연산보다 우선적으로 동작한다.
		 * */
		
		int num1 = 20;
		int num2 = 7;
		
		System.out.println("num1 + num2 = " + (num1 + num2));
		System.out.println("num1 - num2 = " + (num1 - num2));
		System.out.println("num1 * num2 = " + (num1 * num2));
		System.out.println("num1 / num2 = " + (num1 / num2));
		System.out.println("num1 % num2 = " + (num1 % num2));

		
		
		
		
		
		
		
		
	}

}
